const LevelData *level_get(u8 id) {
    if (id >= NUM_LEVELS) return &levels[0];
    return &levels[id];
}

u8 level_get_count(void) {
    return NUM_LEVELS;
}

const char *level_get_name(u8 id) {
    if (id >= NUM_LEVELS) return "???";
    return levels[id].name;
}

const ObstacleDef *level_get_obstacles(u8 id) {
    if (id >= NUM_LEVELS) return level1_obstacles;
    return levels[id].obstacles;
}

